package com.JavaIfElse.Ex;

public class Calculator {
    public Calculator() {
    }


    public int add(int number1, int number2) {
        int add = number1 + number2;
        return add;
    }

    public int diff(int number1, int number2) {
        int diff = number1 - number2;
        return diff;
    }

    public int multiply(int number1, int number2) {
        int multiply = number1 * number2;
        return multiply;
    }

    public float div(int number1, int number2) {
        float div = (float) number1 / (float) number2;
        return div;
    }

    public int add(int number1, int number2, int number3) {
        int add = number1 + number2 + number3;
        return add;
    }

    public float add(float number1, float number2) {
        float add = number1 + number2;
        return add;
    }

    public int add(int number1, int number2, int number3, int number4) {
        int add = number1 + number2 + number3 + number4;
        return add;
    }

    public int diff(int number1, int number2, int number3) {
        int diff = number1 - number2 - number3;
        return diff;
    }

    public float diff(float number1, float number2) {
        float diff = number1 - number2;
        return diff;
    }

    public float multiply(float number1, float number2) {
        float multiply = number1 * number2;
        return multiply;
    }

    public int multiply(int number1, int number2, int number3) {
        int multiply = number1 * number2 * number3;
        return multiply;
    }

}